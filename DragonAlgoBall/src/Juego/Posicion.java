 	package Juego;

public class Posicion {
	private int coordenadaX;
	private int coordenadaY;
	
	public Posicion(int X, int Y){
		this.coordenadaX = X;
		this.coordenadaY = Y;
	}
	public int getCoordenadaX() {
		return coordenadaX;
	}
	public int getCoordenadaY() {
		return coordenadaY;
	}
}
	
